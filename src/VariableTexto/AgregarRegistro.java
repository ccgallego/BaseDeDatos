    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try {
            int dia = Integer.parseInt(txtdianac.getText());
            int mes = Integer.parseInt(txtmesnac.getText());
            short anio = Short.parseShort(txtanonac.getText());
            Persona persona = new Persona(txtnombre.getText(),txtcedula.getText(), dia, mes, anio, txttelefono.getText());
            archi.crearArchivo(persona);
            txtnombre.setText("");
            txttelefono.setText("");
            txtanonac.setText("");
            txtcedula.setText("");
            txtmesnac.setText("");
            txtdianac.setText("");
        } catch (Exception ex) {
            System.out.println(ex);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

